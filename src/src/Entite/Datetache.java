package Entite;

public class Datetache {
private int jour;
private int mois;
private int annee;
public int getJour() {
	return jour;
}
public void setJour(int jour) {
	this.jour = jour;
}
public int getMois() {
	return mois;
}
public void setMois(int mois) {
	this.mois = mois;
}
public int getAnnee() {
	return annee;
}
public void setAnnee(int annee) {
	this.annee = annee;
}

}
